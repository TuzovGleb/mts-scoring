// Генератор deep-research метапромта (шаблон v4).
// Источник шаблона 1:1 — ../../Промт_deep-research_универсальный_v4.md
// Эталон заполнения — ../../Invite_deep-research_промт_v4.md
//
// buildPrompt(answers) детерминированно подставляет ответы опросника в шаблон
// и включает/выключает условные секции (маркетплейс, гео-экспансия).
//
// Формат answers: { [fieldId]: { text: string, tag?: "Ф"|"О"|"Д"|"" } }

const DASH = "—";

function clean(s) {
  return (s == null ? "" : String(s)).trim();
}

// Значение поля без тега.
function val(answers, id, fallback = "") {
  const a = answers[id];
  const t = a ? clean(a.text) : "";
  return t || fallback;
}

// Значение с тегом в скобках: «текст (Ф)». Без тега — просто текст.
function tagged(answers, id, fallback = "") {
  const a = answers[id];
  if (!a) return fallback;
  const t = clean(a.text);
  if (!t) return fallback;
  const tag = clean(a.tag);
  return tag ? `${t} (${tag})` : t;
}

// Эффективный тип бизнеса: если выбрано «Другое» — берём свободный ввод.
function bizType(answers) {
  const sel = val(answers, "biz_type", "односторонний");
  if (sel === "custom") return val(answers, "biz_type_custom", "односторонний");
  return sel;
}

function isMarketplace(answers) {
  const t = bizType(answers).toLowerCase();
  return t.includes("маркетплейс") && !t.includes("не маркетплейс");
}

function expansion(answers) {
  const exp = val(answers, "geo_exp", "нет");
  const has = exp && exp.toLowerCase() !== "нет";
  return { exp, has };
}

// Строка рынка для шапки: «Москва+СПб (ядро) + Дубай (экспансия)» либо «Москва+СПб».
function marketLine(answers) {
  const core = val(answers, "geo_core", "{рынок не указан}");
  const { exp, has } = expansion(answers);
  return has ? `${core} (ядро) + ${exp} (экспансия)` : core;
}

export function buildPrompt(answers) {
  const year = parseInt(val(answers, "year", "2026"), 10) || 2026;
  const yearMinus2 = year - 2;
  const threshold = clean(val(answers, "threshold", "100"));
  const mp = isMarketplace(answers);
  const { exp, has: hasExp } = expansion(answers);
  const businessType = bizType(answers);

  // Продукт: одна фраза + боль, если заполнена.
  const productPhrase = val(answers, "q2", "{что делает — не заполнено}");
  const demand = val(answers, "q19");
  const product = demand
    ? `${productPhrase} Доказательство спроса: ${demand}`
    : productPhrase;

  // Стадия и ресурсы: стадия; команда; ресурсы/burn.
  const stage = val(answers, "q16", "{стадия не указана}");
  const team = val(answers, "q17", "{команда не указана}");
  const resources = val(answers, "q18", "");
  const stageRes = [stage, `команда: ${team}`, resources]
    .filter(Boolean)
    .join("; ");

  const moat = val(answers, "moat") || val(answers, "q15", "{моат не заявлен}");

  // ── Условные куски ──
  const expansionNote = hasExp
    ? `; экспансия рассматривается в ${exp}`
    : "; экспансия не рассматривается";

  // Правило 8 — маркетплейс-оговорка.
  const rule8mp = mp ? "; для маркетплейса также Задача 3" : "";

  // Задача 3 (только маркетплейс).
  const task3 = mp
    ? `3. СТОРОНА ПРЕДЛОЖЕНИЯ: хватает ли участников второй стороны; стоимость их привлечения/удержания; масштабируемость; риск дефицита/дорогого привлечения${
        hasExp ? ` (отдельно по ядру и по ${exp})` : ""
      }.\n`
    : "";

  // Гео-ветка в задаче 4г и формате E.
  const moatGeo = hasExp
    ? `Переносится ли заявленный моат в гео-ядро и в ${exp} (если не переносится — почему).`
    : `Переносится ли заявленный моат в гео-ядро.`;

  const verdictGeo = hasExp
    ? `ВЕРДИКТ отдельно по гео-ядру и по ${exp}: выигрываем / паритет / проигрываем + обоснование.`
    : `ВЕРДИКТ по гео-ядру: выигрываем / паритет / проигрываем + обоснование.`;

  const defensibilityScore = hasExp
    ? `Для «Защищаемости» дай ОТДЕЛЬНЫЙ балл по гео-ядру и по ${exp}.`
    : `Для «Защищаемости» дай балл по гео-ядру.`;

  const competitiveVerdict = hasExp
    ? `E) КОНКУРЕНТНЫЙ ВЕРДИКТ: выигрываем / паритет / проигрываем — отдельно по гео-ядру и по ${exp} + 1 фраза почему. Явно: стоит ли вообще выходить в ${exp} или моат туда не переносится.`
    : `E) КОНКУРЕНТНЫЙ ВЕРДИКТ: выигрываем / паритет / проигрываем по гео-ядру + 1 фраза почему.`;

  const bearExp = hasExp ? ` (хотя бы 1 — по ${exp})` : "";

  // Приоритет усилий (правило 8).
  const effortPriority = `Приоритет усилий: если ресурс ограничен — глубже всего Задача 1 (рынок) и Задача 4 (конкуренты/защищаемость)${rule8mp}.`;

  // ── Сборка ──
  const lines = [];

  lines.push(
    `Ты — аналитик-ресёрчер венчурного фонда. Оцениваю проект для рынка ${marketLine(
      answers
    )} (${year}). Ниже КОНТЕКСТ от основателя (прими как вводные, не пересчитывай и не переспрашивай), затем ПРАВИЛА, ЗАДАЧИ и ФОРМАТ. Цель — вернуть данные так, чтобы по ним можно было выставить балл по факторам.`
  );
  lines.push("");
  lines.push("=== КОНТЕКСТ (вводные) ===");
  lines.push(`Продукт: ${product}`);
  lines.push(`Тип бизнеса: ${businessType}.`);
  lines.push(`Кто платит (ICP): ${val(answers, "q3", "{ICP не указан}")}.`);
  lines.push(`Заменяет: ${val(answers, "q4", "{альтернатива не указана}")}.`);
  lines.push(
    `Гео: ядро ${val(answers, "geo_core", "{гео-ядро не указано}")}${expansionNote}.`
  );
  lines.push(`Стадия и ресурсы: ${stageRes}.`);
  lines.push(`Актив МТС: ${val(answers, "q14", "не используется")}.`);
  lines.push(`Заявленный моат: ${moat}.`);
  lines.push(`Прошлые факты: ${val(answers, "q8", "выручки не было")}.`);
  lines.push("");
  lines.push(
    "Оценки основателя — СНАЧАЛА дай свою НЕЗАВИСИМУЮ оценку, ПОТОМ сравни и вынеси вердикт (подтверждается / завышено / занижено, с источником):"
  );
  lines.push(`- размер рынка / число клиентов: ${tagged(answers, "q5", "не указано")};`);
  lines.push(`- ARPU клиента: ${tagged(answers, "q6", "не указано")};`);
  lines.push(`- захват за 3 года: ${tagged(answers, "q7", "не указано")};`);
  lines.push(`- CAC клиента: ${tagged(answers, "q11", "не указано")};`);
  lines.push(`- удержание клиента: ${tagged(answers, "q12", "не указано")}.`);
  lines.push("");
  lines.push("=== ПРАВИЛА ===");
  lines.push("1. Каждая цифра — источник (ссылка + год) и тег: Факт / Оценка / Гипотеза.");
  lines.push(
    "2. Деньги — в ₽/год, единые единицы; величины — диапазоном low/base/high, не точкой."
  );
  lines.push(
    `3. Приоритет источников ${yearMinus2}–${year}; офиц. статистика, отраслевые отчёты, деловые СМИ. Вендорские блоги дисконтируй и помечай.`
  );
  lines.push("4. Top-down И bottom-up; расхождение >2× — отметь и объясни.");
  lines.push(
    "5. Анти-смещение: активно ищи доказательства, что рынок МЕНЬШЕ, привлечение ТЯЖЕЛЕЕ, а конкуренция СИЛЬНЕЕ, чем считает основатель. Bear-case обязателен."
  );
  lines.push(
    "6. Именованность: конкретные конкуренты/аналоги с названиями, цифрами и ссылками. Без имён — помечай как слабое."
  );
  lines.push(
    "7. Нехватка данных: если прямых данных нет — так и скажи, не выдавай косвенный прокси за факт; предложи, как добыть."
  );
  lines.push(`8. ${effortPriority}`);
  lines.push("9. Нет данных — «данных нет», не выдумывай. Конкретно, без воды.");
  lines.push("");
  lines.push("=== ЗАДАЧИ ===");
  lines.push(
    `1. РАЗМЕР РЫНКА (bottom-up по сегментам): TAM по стране и отдельно SAM по гео-ядру. Сегменты × число клиентов (источник) × реалистичный ARPU/год. Сравни с оценкой основателя.${
      hasExp ? ` Отдельно оцени TAM/SAM по ${exp}.` : ""
    }`
  );
  lines.push(
    "2. ГОТОВНОСТЬ ПЛАТИТЬ: сколько клиенты уже тратят на эту задачу (альтернативы, бюджеты). Реалистичен ли заявленный тариф как доля бюджета?"
  );
  if (task3) lines.push(task3.trimEnd());
  lines.push(
    "4. КОНКУРЕНТЫ И ЗАЩИЩАЕМОСТЬ (углублённо — ключевой раздел):"
  );
  lines.push(
    `   а) 5–8 named конкурентов: страна-ядро + мир${
      hasExp ? ` + ${exp}` : ""
    }. По каждому: бизнес-модель (как зарабатывают), масштаб (выручка/раунды/число клиентов, если есть), позиционирование (на какой сегмент нацелены), их моат.`
  );
  lines.push(
    "   б) Какой объём рынка они уже заняли; рынок консолидирован или фрагментирован."
  );
  lines.push(
    "   в) Барьер входа: насколько легко скопировать продукт; есть ли сетевые эффекты, эксклюзив, данные, переключательные издержки."
  );
  lines.push(
    `   г) МОЖЕМ ЛИ МЫ ВЫИГРАТЬ: в чём наше реальное отличие; где мы сильнее/слабее каждого; что мешает им скопировать нас и нам — их. ${moatGeo} ${verdictGeo}`
  );
  lines.push(
    `5. СРОК ДО ДЕНЕГ: как быстро аналоги выходили на первую выручку и масштаб.`
  );
  lines.push(
    "6. УДЕРЖАНИЕ: бенчмарки churn для этого типа клиента — реалистично ли заявленное удержание?"
  );
  lines.push(
    "7. РИСКИ: регуляторные, репутационные, налоговые/кассовые, защита данных — что схлопывает модель/сегмент." +
      (hasExp ? ` Отдельно — юридические/репутационные риски выхода в ${exp}.` : "")
  );
  lines.push("");
  lines.push("=== ФОРМАТ ОТВЕТА ===");
  lines.push(
    `A) БЛОК ПО ФАКТОРАМ. Скорь то, что добывается ресёрчем: «Размер денег», «Срок до денег», «Юнит-экономика», «Защищаемость/конкуренция». По каждому: данные (low/base/high) + балл 1–5 по шкале ниже + уверенность (Факт/Оценка/Гипотеза) + ключевой источник. ВАЖНО: балл >3 нельзя ставить, если фактор на Гипотезе. Фактор «Реализуемость» НЕ оценивай — пометь «оценивается внутри». ${defensibilityScore}`
  );
  lines.push(
    "   • Размер денег (SOM, ₽/год): 1 = 100–300 млн · 3 = 0,3–1 млрд · 5 = >1 млрд."
  );
  lines.push("   • Срок до денег: 1 = >12 мес · 3 = 6–12 · 5 = <6.");
  lines.push(
    "   • Юнит-экономика (LTV/CAC, окупаемость): 1 = LTV/CAC <1,5 или окуп. >18 мес · 3 = 2–3 / 12–18 · 5 = ≥3 / <12."
  );
  lines.push(
    "   • Защищаемость/конкуренция: 1 = легко копируется, низкий барьер, сильный incumbent · 3 = есть преимущество (связи/дистрибуция/нишевый бренд) · 5 = сильный моат (уникальный рельс/данные/сеть/лицензия)."
  );
  lines.push(
    "B) ТАБЛИЦА claims: [утверждение | low/base/high | источник (ссылка, год) | тег Ф/О/Г]."
  );
  lines.push(
    "C) ВЕРДИКТ по каждой оценке основателя (рынок; ARPU; захват; CAC; удержание; моат)."
  );
  lines.push(
    `D) НЕЗАВИСИМЫЙ SOM по гео-ядру: диапазон ₽/год с явным % захвата SAM (для нишевого B2B ориентир 3–10% за 3 года, обоснуй) и горизонтом. Проходит ли порог ≥${threshold} млн ₽/год — да/нет.${
      hasExp ? ` Отдельно: ориентировочный SOM по ${exp}, если выход состоится.` : ""
    }`
  );
  lines.push(competitiveVerdict);
  lines.push(
    `F) BEAR-CASE: 3 причины провала${bearExp} + сильнейшее доказательство, что проект меньше/тяжелее/конкурентнее, чем кажется.`
  );

  return lines.join("\n");
}

// Краткий заголовок для файла/превью.
export function promptTitle(answers) {
  const name = val(answers, "q1", "проект");
  return `${name} — deep-research метапромт (v4)`;
}
